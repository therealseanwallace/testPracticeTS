const calc = {
  add: (a: number, b: number): number => {
    return a + b
  },
  sub: (a: number, b: number): number => {
    return a - b
  },
  multiply: (a: number, b: number): number => {
    return a * b
  },
  divide: (a: number, b: number): number => {
    return a / b
  },
}

export default calc;